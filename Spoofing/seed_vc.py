import os
import sys
import uuid
import soundfile as sf
import importlib.util

# ================================
# ROOT PATH
# ================================
SEED_PROJECT_PATH = "/content/seed-vc"

# (اختياري) يساعد بعض الحالات لكنه مو الأساسي الآن
if SEED_PROJECT_PATH not in sys.path:
    sys.path.insert(0, SEED_PROJECT_PATH)

_MODEL = None


def get_model():
    global _MODEL

    if _MODEL is None:

        # ================================
        # FIX: تحميل الملف مباشرة بدون import مشاكل
        # ================================
        inference_path = os.path.join(SEED_PROJECT_PATH, "inference_v2.py")

        spec = importlib.util.spec_from_file_location(
            "inference_v2",
            inference_path
        )
        inference_v2 = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(inference_v2)

        convert_voice_v2 = inference_v2.convert_voice_v2

        class SeedArgs:
            def __init__(self):
                # 🔥 أسرع إعدادات
                self.diffusion_steps = 10
                self.length_adjust = 1.0
                self.intelligibility_cfg_rate = 0.7
                self.similarity_cfg_rate = 0.75
                self.top_p = 0.9
                self.temperature = 1.0
                self.repetition_penalty = 1.0
                self.convert_style = True
                self.anonymization_only = False
                self.compile = False

                # checkpoint
                self.ar_checkpoint_path = os.path.join(
                    SEED_PROJECT_PATH,
                    "DiT_seed_v2_uvit_whisper_small_wavenet_bigvgan_pruned.pth"
                )
                self.cfm_checkpoint_path = None

        _MODEL = {
            "fn": convert_voice_v2,
            "args": SeedArgs()
        }

    return _MODEL


def run_seed(src_audio, tgt_audio, sr=16000):

    model = get_model()
    fn = model["fn"]
    args = model["args"]

    uid = str(uuid.uuid4())[:8]

    src_path = f"/tmp/src_{uid}.wav"
    tgt_path = f"/tmp/tgt_{uid}.wav"
    out_path = f"/tmp/out_{uid}.wav"

    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    try:
        result = fn(src_path, tgt_path, args)

        if result is None:
            raise RuntimeError("Seed-VC failed")

        sr_out, audio = result
        sf.write(out_path, audio, sr_out)

        return out_path

    finally:
        if os.path.exists(src_path): os.remove(src_path)
        if os.path.exists(tgt_path): os.remove(tgt_path)