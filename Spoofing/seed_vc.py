# -*- coding: utf-8 -*-

import os
import uuid
import subprocess
import soundfile as sf
import glob
import torch
import numpy as np

# 📌 مسار الريبو (ثابت)
SEED_DIR = "/content/Robust-Multimodal-Age-Verification-for-Online-Gaming-Chat/seed-vc"

# 📌 مجلد مؤقت خارج الريبو (مهم جداً عشان ما “يحفظ في git”)
TMP_DIR = "/content/tmp_seed"
os.makedirs(TMP_DIR, exist_ok=True)


def run_seed(src_audio, tgt_audio, sr=16000):

    # =========================
    # 1. تجهيز الملفات
    # =========================
    uid = str(uuid.uuid4())[:8]

    src_path = os.path.join(TMP_DIR, f"{uid}_src.wav")
    tgt_path = os.path.join(TMP_DIR, f"{uid}_tgt.wav")

    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    # =========================
    # 2. تشغيل Seed-VC
    # =========================
    print("🚀 Running Seed-VC...")

    cmd = [
        "python",
        "inference_v2.py",
        "--source", src_path,
        "--target", tgt_path,
        "--output", TMP_DIR,
        "--diffusion-steps", "50",
        "--similarity-cfg-rate", "0.75",
        "--convert-style", "True"   # لازم String صحيحة
    ]

    process = subprocess.Popen(
        cmd,
        cwd=SEED_DIR,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        bufsize=1
    )

    for line in process.stdout:
        print(line, end="")

    process.wait()

    if process.returncode != 0:
        raise RuntimeError("❌ Seed-VC failed")

    # =========================
    # 3. جلب الناتج بشكل آمن
    # =========================
    wavs = sorted(glob.glob(os.path.join(TMP_DIR, "*.wav")), key=os.path.getctime)

    if not wavs:
        raise RuntimeError("❌ No output generated")

    out_path = wavs[-1]

    # =========================
    # 4. تحويله إلى نفس أسلوب KNN/KOKO
    # =========================
    audio, _ = sf.read(out_path)

    # optional: تنظيف الملفات المؤقتة (مهم لتسريع لاحقاً)
    try:
        os.remove(src_path)
        os.remove(tgt_path)
    except:
        pass

    return audio, sr