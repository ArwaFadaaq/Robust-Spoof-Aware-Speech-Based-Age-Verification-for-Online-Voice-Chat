import os
import uuid
import soundfile as sf

_MODEL_LOADED = False


def run_seed(src_audio, tgt_audio, sr=16000):

    global _MODEL_LOADED

    if not _MODEL_LOADED:
        _MODEL_LOADED = True

    src_path = f"/content/{uuid.uuid4()}_src.wav"
    tgt_path = f"/content/{uuid.uuid4()}_tgt.wav"
    out_dir = "/content/seed_out"

    os.makedirs(out_dir, exist_ok=True)

    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    os.system(f"""
    python seed-vc/inference_v2.py \
        --source "{src_path}" \
        --target "{tgt_path}" \
        --output "{out_dir}" \
        --diffusion-steps 50 \
        --similarity-cfg-rate 0.75 \
        --convert-style True
    """)

    wavs = [f for f in os.listdir(out_dir) if f.endswith(".wav")]
    return os.path.join(out_dir, sorted(wavs)[-1])