import os
import uuid
import subprocess
import soundfile as sf
import glob
import numpy as np

SEED_DIR = "/content/Robust-Multimodal-Age-Verification-for-Online-Gaming-Chat/seed-vc"
OUTPUT_DIR = os.path.join(SEED_DIR, "output_results")


def run_seed(src_audio, tgt_audio, sr=16000):

    uid = str(uuid.uuid4())[:8]

    src_path = f"/content/s_{uid}.wav"
    tgt_path = f"/content/t_{uid}.wav"

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    print("🚀 Running Seed-VC...")

    cmd = [
        "python",
        "inference_v2.py",
        "--source", src_path,
        "--target", tgt_path,
        "--output", OUTPUT_DIR,
        "--diffusion-steps", "50",
        "--similarity-cfg-rate", "0.75",
        "--convert-style", "True"
    ]

    process = subprocess.Popen(
        cmd,
        cwd=SEED_DIR,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True
    )

    for line in process.stdout:
        print(line, end="")

    process.wait()

    if process.returncode != 0:
        raise RuntimeError("Seed VC failed")

    # 🔥 أهم تعديل: نرجع الصوت بدل path
    wavs = glob.glob(os.path.join(OUTPUT_DIR, "*.wav"))
    if not wavs:
        raise RuntimeError("No output generated")

    latest = max(wavs, key=os.path.getctime)

    audio, sr = sf.read(latest)

    # 🧼 تنظيف (اختياري لكن مهم)
    try:
        os.remove(src_path)
        os.remove(tgt_path)
    except:
        pass

    return audio, sr