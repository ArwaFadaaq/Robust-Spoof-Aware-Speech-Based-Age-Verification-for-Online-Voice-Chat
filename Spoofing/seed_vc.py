import os
import sys
import uuid
import soundfile as sf

# ================================
# PATH (Colab)
# ================================
SEED_DIR = "/content/seed-vc"

if SEED_DIR not in sys.path:
    sys.path.insert(0, SEED_DIR)

# ================================
# GLOBAL CACHE
# ================================
_MODEL = None


def _load_model_once():
    """
    Load model only once (Lazy Loading)
    """
    global _MODEL

    if _MODEL is not None:
        print("[SeedVC] Model already loaded ✔")
        return _MODEL

    print("[SeedVC] Loading model for the FIRST time... ⏳")

    # 🔥 FIX: ensure configs path works
    os.chdir(SEED_DIR)

    try:
        from inference_v2 import convert_voice_v2
    except ImportError as e:
        raise ImportError(
            f"❌ inference_v2 not found in {SEED_DIR}"
        ) from e

    class SeedArgs:
        def __init__(self):
            # 🔥 speed settings
            self.diffusion_steps = 10
            self.length_adjust = 1.0
            self.intelligibility_cfg_rate = 0.7
            self.similarity_cfg_rate = 0.75
            self.top_p = 0.9
            self.temperature = 1.0
            self.repetition_penalty = 1.0
            self.convert_style = True
            self.anonymization_only = False
            self.compile = False

            # checkpoint path
            self.ar_checkpoint_path = os.path.join(
                SEED_DIR,
                "checkpoints/DiT_seed_v2_uvit_whisper_small_wavenet_bigvgan_pruned.pth"
            )
            self.cfm_checkpoint_path = None

    _MODEL = {
        "fn": convert_voice_v2,
        "args": SeedArgs()
    }

    print("[SeedVC] Model loaded successfully 🚀")
    return _MODEL


# ================================
# MAIN FUNCTION (used by voice_conversion.py)
# ================================
def run_seed(src_audio, tgt_audio, sr=16000):
    """
    Compatible with:
    run_vc_on_file(source, target, "seed")
    """

    print("\n[SeedVC v11] 🚀 Running Seed-VC conversion...")

    model = _load_model_once()
    fn = model["fn"]
    args = model["args"]

    uid = str(uuid.uuid4())[:8]

    src_path = f"/tmp/src_{uid}.wav"
    tgt_path = f"/tmp/tgt_{uid}.wav"
    out_path = f"/tmp/out_{uid}.wav"

    # save numpy → wav
    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    try:
        # 🔥 FIX: ensure correct working directory during run
        old_cwd = os.getcwd()
        os.chdir(SEED_DIR)

        result = fn(src_path, tgt_path, args)

        os.chdir(old_cwd)

        if result is None:
            raise RuntimeError("Seed-VC returned None")

        sr_out, audio = result
        sf.write(out_path, audio, sr_out)

        print("[SeedVC] Done ✔")
        return out_path

    finally:
        # cleanup
        if os.path.exists(src_path):
            os.remove(src_path)
        if os.path.exists(tgt_path):
            os.remove(tgt_path)