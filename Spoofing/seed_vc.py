import os
import uuid
import subprocess
import soundfile as sf
import shutil

SEED_DIR = "/content/seed-vc"
# يفضل إنشاء مجلد فرعي فريد لكل عملية لتجنب IndexError
TEMP_OUTPUT = os.path.join(SEED_DIR, "temp_results")

def run_seed(src_audio, tgt_audio, sr=16000):
    # 1. تجهيز المسارات
    u_id = str(uuid.uuid4())
    src_path = f"/content/{u_id}_src.wav"
    tgt_path = f"/content/{u_id}_tgt.wav"

    # تنظيف أو إنشاء مجلد المخرجات المؤقت
    if os.path.exists(TEMP_OUTPUT):
        shutil.rmtree(TEMP_OUTPUT)
    os.makedirs(TEMP_OUTPUT, exist_ok=True)

    # 2. حفظ الملفات المؤقتة
    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    # 3. بناء الأمر (Command)
    # ملاحظة: تم إزالة "True" لأن السكربت غالباً يستخدمها كـ Flag
    cmd = [
        "python", "inference_v2.py",
        "--source", src_path,
        "--target", tgt_path,
        "--output", TEMP_OUTPUT,
        "--diffusion-steps", "50",
        "--similarity-cfg-rate", "0.75",
        "--convert-style" # إذا كان السكربت يطلب قيمة جرب إضافة "True" مرة أخرى
    ]

    # 4. التنفيذ
    result = subprocess.run(
        cmd,
        cwd=SEED_DIR,
        capture_output=True,
        text=True
    )

    if result.returncode != 0:
        print("STDOUT:", result.stdout)
        print("STDERR:", result.stderr)
        raise RuntimeError(f"Seed VC failed with error: {result.stderr}")

    # 5. جلب الملف الناتج بدقة
    wavs = [os.path.join(TEMP_OUTPUT, f) for f in os.listdir(TEMP_OUTPUT) if f.endswith(".wav")]

    if not wavs:
        # محاولة أخيرة: البحث في المجلد الرئيسي لـ Seed-VC
        # لأن بعض النسخ تحفظ في المجلد الحالي
        wavs = [os.path.join(SEED_DIR, f) for f in os.listdir(SEED_DIR) if f.endswith(".wav") and u_id in f]

    if not wavs:
        raise RuntimeError("Seed VC did not generate any output file. Check the logs above.")

    # نأخذ أحدث ملف تم إنشاؤه
    latest_output = max(wavs, key=os.path.getctime)

    return latest_output