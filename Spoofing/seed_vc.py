import os
import uuid
import subprocess
import soundfile as sf

# 1. حدد المسار المطلق لمجلد الموديل
SEED_DIR = "/content/seed-vc"

def run_seed(src_audio, tgt_audio, sr=16000):
    # إنشاء أسماء ملفات فريدة
    u_id = str(uuid.uuid4())[:8]
    src_path = f"/content/src_{u_id}.wav"
    tgt_path = f"/content/tgt_{u_id}.wav"
    out_dir = f"/content/output_{u_id}" # مجلد مخرجات فريد لكل عملية

    os.makedirs(out_dir, exist_ok=True)

    # حفظ مصفوفات الصوت لملفات (لأن الموديل يطلب ملفات)
    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    # 2. بناء الأمر تماماً كما كنت تكتبه في خلية كولاب
    # لاحظ استخدام f-string ووضع "True" كنص صريح
    cmd = (
        f"python inference_v2.py "
        f"--source '{src_path}' "
        f"--target '{tgt_path}' "
        f"--output '{out_dir}' "
        f"--diffusion-steps 50 "
        f"--similarity-cfg-rate 0.75 "
        f"--convert-style True"
    )

    print(f"Running command in {SEED_DIR}...")

    # 3. التنفيذ باستخدام shell=True (هذا هو المفتاح لمحاكاة علامة !)
    # الـ cwd تجعل البايثون "يدخل" للمجلد قبل تشغيل الأمر
    process = subprocess.run(
        cmd,
        cwd=SEED_DIR,
        shell=True,
        capture_output=True,
        text=True
    )

    # فحص الأخطاء (لو فشل، سيطبع لك السبب بالضبط من السكربت نفسه)
    if process.returncode != 0:
        print("Error details from Seed-VC:")
        print(process.stderr)
        raise RuntimeError(f"Inference failed: {process.stderr}")

    # 4. العثور على الملف الناتج
    import glob
    generated_files = glob.glob(f"{out_dir}/*.wav")

    if not generated_files:
        raise RuntimeError("Seed-VC finished but no .wav file was found in output folder.")

    return generated_files[0]