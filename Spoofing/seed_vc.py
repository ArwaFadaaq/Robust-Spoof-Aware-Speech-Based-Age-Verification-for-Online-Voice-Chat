import os
import uuid
import subprocess
import soundfile as sf

SEED_DIR = "/content/seed-vc"
OUTPUT_DIR = os.path.join(SEED_DIR, "output_results")

def run_seed(src_audio, tgt_audio, sr=16000):

    src_path = f"/content/{uuid.uuid4()}_src.wav"
    tgt_path = f"/content/{uuid.uuid4()}_tgt.wav"

    sf.write(src_path, src_audio, sr)
    sf.write(tgt_path, tgt_audio, sr)

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    cmd = [
        "python",
        "inference_v2.py",
        "--source", src_path,
        "--target", tgt_path,
        "--output", OUTPUT_DIR,
        "--diffusion-steps", "50",
        "--similarity-cfg-rate", "0.75",
        "--convert-style", "True"
    ]

    result = subprocess.run(
        cmd,
        cwd=SEED_DIR,
        capture_output=True,
        text=True
    )

    if result.returncode != 0:
        print(result.stdout)
        print(result.stderr)
        raise RuntimeError("Seed VC failed")

    wavs = [f for f in os.listdir(OUTPUT_DIR) if f.endswith(".wav")]

    if len(wavs) == 0:
        raise RuntimeError("Seed VC did not generate output")

    latest = sorted(wavs)[-1]

    return os.path.join(OUTPUT_DIR, latest)