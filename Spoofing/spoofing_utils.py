import pandas as pd
import os

def build_transcript_inventory(
    source_csv,
    manifest_csv,
    metadata_csv,
    output_dir,
    age_col="mapped_age_class",
    split_col="split"
):
    """
    Builds transcript inventory and saves:
    train/val/test × adult/minor transcripts
    """

    # Load files
    df_source = pd.read_csv(source_csv)
    df_manifest = pd.read_csv(manifest_csv)
    df_meta = pd.read_csv(metadata_csv)

    # Merge 1: parent_file_id -> path
    df_merged = df_source.merge(
        df_manifest,
        left_on="parent_file_id",
        right_on="file_id",
        how="left"
    )

    # Merge 2: path -> sentence
    df_merged = df_merged.merge(
        df_meta,
        left_on="path",
        right_on="clips_path",
        how="left"
    )

    # Select needed columns
    result = df_merged[
        [
            "segment_id",
            "sentence_id",
            "sentence",
            "clips_path",
            age_col,
            split_col
        ]
    ]

    # Flags (no dropping NaN)
    result["has_transcript"] = result["sentence"].notna()
    result["is_matched"] = result["clips_path"].notna()

    os.makedirs(output_dir, exist_ok=True)

    # Splits
    splits = result[split_col].unique()

    for sp in splits:
        split_df = result[result[split_col] == sp]

        adult_df = split_df[split_df[age_col] == "adult"]
        minor_df = split_df[split_df[age_col] == "minor"]

        adult_path = os.path.join(
            output_dir,
            f"{sp}_spoof_source_adult_transcripts.csv"
        )

        minor_path = os.path.join(
            output_dir,
            f"{sp}_spoof_source_minor_transcripts.csv"
        )

        adult_df.to_csv(adult_path, index=False)
        minor_df.to_csv(minor_path, index=False)

        print(f"\n✅ Saved for split: {sp}")
        print("Adult:", len(adult_df), "→", adult_path)
        print("Minor:", len(minor_df), "→", minor_path)

    return result