import pandas as pd
import os

def build_transcript_inventory(
    source_csv,
    manifest_csv,
    metadata_csv,
    output_csv,
    age_col="mapped_age_class"
):

    # Load files
    df_source = pd.read_csv(source_csv)
    df_manifest = pd.read_csv(manifest_csv, low_memory=False)
    df_meta = pd.read_csv(metadata_csv)

    df_source = df_source[df_source["dataset_source"] == "common_voice"]

    print(f"After filtering common_voice: {len(df_source)} rows")

    # Merge 1: parent_file_id -> path
    df_merged = df_source.merge(
        df_manifest,
        left_on="parent_file_id",
        right_on="file_id",
        how="left"
    )

    # Merge 2: path -> sentence
    df_merged = df_merged.merge(
        df_meta,
        left_on="path",
        right_on="clips_path",
        how="left"
    )

    # Select needed columns
    result = df_merged[
        [
            "segment_id",
            "sentence_id",
            "sentence",
            "clips_path",
            age_col
        ]
    ].copy()

    # Debug flags
    result.loc[:, "has_transcript"] = result["sentence"].notna()
    result.loc[:, "is_matched"] = result["clips_path"].notna()

    # Save
    os.makedirs(os.path.dirname(output_csv), exist_ok=True)
    result.to_csv(output_csv, index=False)

    print(f"\n✅ Saved transcript file:")
    print(output_csv)
    print(f"Total rows: {len(result)}")
    print(f"With transcript: {result['has_transcript'].sum()}")
    print(f"Without transcript: {(~result['has_transcript']).sum()}")

    return result