        "# -*- coding: utf-8 -*-\n",
        "\"\"\"\n",
        "Audio Preprocessing Module\n",
        "\n",
        "This module implements the audio preprocessing pipeline used for the\n",
        "age verification experiments.\n",
        "\n",
        "Supported datasets\n",
        "------------------\n",
        "1. Common Voice\n",
        "   - Recordings may have different sampling rates.\n",
        "   - Audio is resampled to 16 kHz.\n",
        "   - Silence is removed from the beginning and end of the recording.\n",
        "\n",
        "2. MyST\n",
        "   - Audio is assumed to already use the target sampling rate.\n",
        "   - Only silence trimming and segmentation are applied.\n",
        "\n",
        "Processing Pipeline\n",
        "-------------------\n",
        "1. Load the audio recording.\n",
        "2. Ensure the waveform is single-channel.\n",
        "3. (Common Voice only) Resample audio to the target sampling rate.\n",
        "4. Remove leading and trailing silence.\n",
        "5. Optionally remove internal silence using voice activity detection.\n",
        "6. Segment the waveform into fixed-length overlapping windows.\n",
        "\n",
        "These segments are later used as inputs for the training pipeline.\n",
        "\"\"\"\n",
        "\n",
        "import os\n",
        "import torch\n",
        "import torchaudio\n",
        "import librosa\n",
        "from silero_vad import load_silero_vad, get_speech_timestamps\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Global Configuration\n",
        "# =========================================================\n",
        "\n",
        "# Load the Silero VAD model once during import\n",
        "silero_model = load_silero_vad()\n",
        "\n",
        "# Target sampling rate used across the project\n",
        "TARGET_SR = 16000\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Basic Audio Utilities\n",
        "# =========================================================\n",
        "def load_audio(audio_path: str):\n",
        "    \"\"\"\n",
        "    Load an audio file from disk.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    audio_path : str\n",
        "        Path to the audio file.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    signal : torch.Tensor\n",
        "        Audio waveform tensor.\n",
        "    sr : int\n",
        "        Sampling rate of the audio file.\n",
        "    \"\"\"\n",
        "    signal, sr = torchaudio.load(audio_path)\n",
        "    return signal, sr\n",
        "\n",
        "\n",
        "def ensure_single_channel(signal: torch.Tensor) -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Convert audio to a single-channel waveform.\n",
        "\n",
        "    If the audio contains multiple channels, they are averaged to produce\n",
        "    a mono waveform.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    signal : torch.Tensor\n",
        "        Input waveform tensor.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    torch.Tensor\n",
        "        Single-channel waveform.\n",
        "    \"\"\"\n",
        "    if signal.ndim == 2 and signal.shape[0] > 1:\n",
        "        signal = signal.mean(dim=0, keepdim=True)\n",
        "\n",
        "    if signal.ndim == 2:\n",
        "        return signal.squeeze(0)\n",
        "\n",
        "    return signal\n",
        "\n",
        "\n",
        "def resample_audio(\n",
        "    wav: torch.Tensor,\n",
        "    orig_sr: int,\n",
        "    target_sr: int = TARGET_SR\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Resample a waveform to the target sampling rate.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    wav : torch.Tensor\n",
        "        Input waveform.\n",
        "    orig_sr : int\n",
        "        Original sampling rate.\n",
        "    target_sr : int\n",
        "        Target sampling rate.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    torch.Tensor\n",
        "        Resampled waveform.\n",
        "    \"\"\"\n",
        "    if orig_sr != target_sr:\n",
        "        resampler = torchaudio.transforms.Resample(orig_sr, target_sr)\n",
        "        wav = resampler(wav.unsqueeze(0)).squeeze(0)\n",
        "\n",
        "    return wav\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Silence Processing\n",
        "# =========================================================\n",
        "def remove_leading_trailing_silence(\n",
        "    wav: torch.Tensor,\n",
        "    top_db: int = 30\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Remove silence from the beginning and end of a waveform.\n",
        "\n",
        "    Internal pauses within the recording are preserved.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    wav : torch.Tensor\n",
        "        Input waveform.\n",
        "    top_db : int\n",
        "        Silence threshold in decibels.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    torch.Tensor\n",
        "        Trimmed waveform.\n",
        "    \"\"\"\n",
        "    wav_np = wav.cpu().numpy()\n",
        "\n",
        "    intervals = librosa.effects.split(wav_np, top_db=top_db)\n",
        "\n",
        "    if len(intervals) == 0:\n",
        "        return wav\n",
        "\n",
        "    start = intervals[0][0]\n",
        "    end = intervals[-1][1]\n",
        "\n",
        "    trimmed = wav_np[start:end]\n",
        "\n",
        "    return torch.tensor(trimmed, dtype=torch.float32)\n",
        "\n",
        "\n",
        "def remove_internal_silence_vad(\n",
        "    wav: torch.Tensor,\n",
        "    sr: int = TARGET_SR\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Remove internal non-speech regions using voice activity detection.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    wav : torch.Tensor\n",
        "        Input waveform.\n",
        "    sr : int\n",
        "        Sampling rate.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    torch.Tensor\n",
        "        Waveform containing only detected speech segments.\n",
        "    \"\"\"\n",
        "    wav = wav.float().cpu()\n",
        "\n",
        "    speech_timestamps = get_speech_timestamps(\n",
        "        wav,\n",
        "        silero_model,\n",
        "        sampling_rate=sr\n",
        "    )\n",
        "\n",
        "    if len(speech_timestamps) == 0:\n",
        "        return torch.empty(0, dtype=torch.float32)\n",
        "\n",
        "    speech_segments = [\n",
        "        wav[item[\"start\"]:item[\"end\"]]\n",
        "        for item in speech_timestamps\n",
        "    ]\n",
        "\n",
        "    return torch.cat(speech_segments)\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Segmentation\n",
        "# =========================================================\n",
        "def segment_waveform(\n",
        "    wav: torch.Tensor,\n",
        "    sr: int = TARGET_SR,\n",
        "    seg_sec: float = 3.0,\n",
        "    hop_sec: float = 1.5\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Split a waveform into overlapping fixed-length segments.\n",
        "\n",
        "    Parameters\n",
        "    ----------\n",
        "    wav : torch.Tensor\n",
        "        Input waveform.\n",
        "    sr : int\n",
        "        Sampling rate.\n",
        "    seg_sec : float\n",
        "        Segment length in seconds.\n",
        "    hop_sec : float\n",
        "        Hop length in seconds.\n",
        "\n",
        "    Returns\n",
        "    -------\n",
        "    torch.Tensor\n",
        "        Tensor containing all generated segments.\n",
        "    \"\"\"\n",
        "    seg_len = int(sr * seg_sec)\n",
        "    hop_len = int(sr * hop_sec)\n",
        "\n",
        "    if wav.numel() < seg_len:\n",
        "        return torch.empty(0, dtype=torch.float32)\n",
        "\n",
        "    frames = librosa.util.frame(\n",
        "        wav.cpu().numpy(),\n",
        "        frame_length=seg_len,\n",
        "        hop_length=hop_len\n",
        "    ).T\n",
        "\n",
        "    return torch.from_numpy(frames.copy()).float()\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Dataset-Specific Preprocessing\n",
        "# =========================================================\n",
        "def preprocess_common_voice_audio(\n",
        "    audio_path: str,\n",
        "    target_sr: int = TARGET_SR,\n",
        "    top_db: int = 30,\n",
        "    seg_sec: float = 3.0,\n",
        "    hop_sec: float = 1.5,\n",
        "    remove_internal_silence: bool = False\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Apply preprocessing pipeline for Common Voice recordings.\n",
        "    \"\"\"\n",
        "\n",
        "    signal, sr = load_audio(audio_path)\n",
        "\n",
        "    wav = ensure_single_channel(signal).float()\n",
        "\n",
        "    wav = resample_audio(wav, sr, target_sr)\n",
        "\n",
        "    wav = remove_leading_trailing_silence(wav, top_db=top_db)\n",
        "\n",
        "    if remove_internal_silence:\n",
        "        wav = remove_internal_silence_vad(wav, sr=target_sr)\n",
        "\n",
        "    if wav.numel() == 0:\n",
        "        return torch.empty(0, dtype=torch.float32)\n",
        "\n",
        "    return segment_waveform(\n",
        "        wav,\n",
        "        sr=target_sr,\n",
        "        seg_sec=seg_sec,\n",
        "        hop_sec=hop_sec\n",
        "    )\n",
        "\n",
        "\n",
        "def preprocess_myst_audio(\n",
        "    audio_path: str,\n",
        "    sr: int = TARGET_SR,\n",
        "    top_db: int = 30,\n",
        "    seg_sec: float = 3.0,\n",
        "    hop_sec: float = 1.5,\n",
        "    remove_internal_silence: bool = False\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Apply preprocessing pipeline for MyST recordings.\n",
        "    \"\"\"\n",
        "\n",
        "    signal, _ = load_audio(audio_path)\n",
        "\n",
        "    wav = ensure_single_channel(signal).float()\n",
        "\n",
        "    wav = remove_leading_trailing_silence(wav, top_db=top_db)\n",
        "\n",
        "    if remove_internal_silence:\n",
        "        wav = remove_internal_silence_vad(wav, sr=sr)\n",
        "\n",
        "    if wav.numel() == 0:\n",
        "        return torch.empty(0, dtype=torch.float32)\n",
        "\n",
        "    return segment_waveform(\n",
        "        wav,\n",
        "        sr=sr,\n",
        "        seg_sec=seg_sec,\n",
        "        hop_sec=hop_sec\n",
        "    )\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Dispatcher\n",
        "# =========================================================\n",
        "def preprocess_by_dataset(\n",
        "    audio_path: str,\n",
        "    dataset_name: str,\n",
        "    target_sr: int = TARGET_SR,\n",
        "    top_db: int = 30,\n",
        "    seg_sec: float = 3.0,\n",
        "    hop_sec: float = 1.5,\n",
        "    remove_internal_silence: bool = False\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Select the appropriate preprocessing pipeline based on dataset name.\n",
        "    \"\"\"\n",
        "    dataset_name = dataset_name.lower()\n",
        "\n",
        "    if dataset_name == \"cv\":\n",
        "        return preprocess_common_voice_audio(\n",
        "            audio_path=audio_path,\n",
        "            target_sr=target_sr,\n",
        "            top_db=top_db,\n",
        "            seg_sec=seg_sec,\n",
        "            hop_sec=hop_sec,\n",
        "            remove_internal_silence=remove_internal_silence\n",
        "        )\n",
        "\n",
        "    if dataset_name == \"myst\":\n",
        "        return preprocess_myst_audio(\n",
        "            audio_path=audio_path,\n",
        "            sr=target_sr,\n",
        "            top_db=top_db,\n",
        "            seg_sec=seg_sec,\n",
        "            hop_sec=hop_sec,\n",
        "            remove_internal_silence=remove_internal_silence\n",
        "        )\n",
        "\n",
        "    raise ValueError(f\"Unknown dataset_name: {dataset_name}\")\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Model Input Utility\n",
        "# =========================================================\n",
        "def load_segment_for_model(\n",
        "    audio_path: str,\n",
        "    target_sr: int = TARGET_SR\n",
        ") -> torch.Tensor:\n",
        "    \"\"\"\n",
        "    Load a saved audio segment and prepare it for model input.\n",
        "    \"\"\"\n",
        "    signal, sr = load_audio(audio_path)\n",
        "\n",
        "    wav = ensure_single_channel(signal).float()\n",
        "\n",
        "    wav = resample_audio(wav, sr, target_sr)\n",
        "\n",
        "    return wav\n",
        "\n",
        "\n",
        "# =========================================================\n",
        "# Segment Saving\n",
        "# =========================================================\n",
        "def save_segment_as_wav(\n",
        "    segment: torch.Tensor,\n",
        "    output_path: str,\n",
        "    sr: int = TARGET_SR\n",
        "):\n",
        "    \"\"\"\n",
        "    Save a single audio segment to disk as a WAV file.\n",
        "    \"\"\"\n",
        "\n",
        "    os.makedirs(os.path.dirname(output_path), exist_ok=True)\n",
        "\n",
        "    if segment.ndim == 1:\n",
        "        segment = segment.unsqueeze(0)\n",
        "\n",
        "    torchaudio.save(output_path, segment.cpu(), sr)"
