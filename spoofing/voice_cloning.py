    ipd.display(ipd.Audio(ref_file))

    output_path = '/content/output_cloned.wav'
    convert_single(model, source_file, ref_file, output_path)

    print("\n Results:")
    print("  Source voice:")
    ipd.display(ipd.Audio(source_file))
    print("  Reference voice:")
    ipd.display(ipd.Audio(ref_file))
    print("  Converted voice:")
    ipd.display(ipd.Audio(output_path))

