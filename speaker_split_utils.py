"""
Speaker-Level Stratified Splitting Pipeline
------------------------------------------

This module implements a speaker-disjoint stratified data splitting pipeline
for audio-based datasets.

Objective
---------
To partition datasets into training, validation, and test splits while:
- Preventing speaker overlap across splits (no data leakage).
- Preserving key data distributions (e.g., dataset source, gender, and age).

Methodology
-----------
1. Clean and normalize metadata fields.
2. Derive auxiliary features (e.g., age_bin) for stratification.
3. Construct a stratification key based on dataset type:
   - Adult datasets: (dataset_source, gender, age_bin)
   - Minor datasets: (dataset_source, gender)
4. Perform stratified splitting at the speaker level.
5. Assign speakers to train/val/test splits according to predefined ratios.
6. Validate that no speaker appears in multiple splits.

Output
------
- A CSV file per dataset containing:
  - Original metadata
  - Assigned split label (train / val / test)

Notes
-----
- Auxiliary columns (e.g., strat_key, age_bin) are used internally
  during processing and are removed before saving the final output.
- The pipeline is deterministic given a fixed random seed.
"""

import os
import numpy as np
import pandas as pd
from sklearn.model_selection import GroupShuffleSplit


# ─── Column Cleaning ─────────────────────────────────────
def clean_col(df: pd.DataFrame, col: str, fill_value: str = "unknown") -> pd.DataFrame:
    """
    Standardize a metadata column by:
    1. Replacing missing values with a default label.
    2. Converting values to string format.
    3. Removing leading/trailing whitespace.

    This ensures consistency before downstream processing.
    """
    if col in df.columns:
        df[col] = df[col].astype(object).where(~df[col].isna(), fill_value)
        df[col] = df[col].astype(str).str.strip()
    return df


# ─── Age Mapping ─────────────────────────────────────────
def map_age_to_bin(age) -> str:
    """
    Map raw age values into unified categorical bins.

    Supported bins:
    - twenties, thirties, fourties, fifties, sixties, 70+

    Handles both string labels and numeric ages.
    """
    age = str(age).strip().lower()

    if age in ["twenties", "thirties", "fourties", "fifties", "sixties"]:
        return age
    if age in ["seventies", "eighties", "nineties"]:
        return "70+"

    try:
        age_num = int(float(age))
        if age_num < 30:
            return "twenties"
        elif age_num < 40:
            return "thirties"
        elif age_num < 50:
            return "fourties"
        elif age_num < 60:
            return "fifties"
        elif age_num < 70:
            return "sixties"
        else:
            return "70+"
    except Exception:
        return "unknown"


# ─── Data Cleaning Pipeline ──────────────────────────────
def clean_dataframe(df: pd.DataFrame) -> pd.DataFrame:
    """
    Validate and normalize the speaker-level dataset.

    Key steps:
    - Ensure required column (speaker_id) exists.
    - Normalize metadata fields (dataset_source, gender, age).
    - Generate age_bin for consistent grouping.
    """
    df = df.copy()

    if "speaker_id" not in df.columns:
        raise ValueError("Missing required column: speaker_id")

    df["speaker_id"] = df["speaker_id"].astype(str).str.strip()

    for col in ["dataset_source", "gender", "source_age"]:
        if col in df.columns:
            df = clean_col(df, col, fill_value="unknown")

    if "source_age" in df.columns:
        df["age_bin"] = df["source_age"].apply(map_age_to_bin)

    return df


# ─── Stratification Key ──────────────────────────────────
def build_strat_key(speakers_df: pd.DataFrame, dataset_name: str) -> pd.Series:
    """
    Construct a stratification key used to preserve data distribution
    across splits.

    Adult datasets → stratify by (dataset_source, gender, age_bin)
    Minor datasets → stratify by (dataset_source, gender)
    """
    speakers_df = speakers_df.copy()

    if "dataset_source" not in speakers_df.columns:
        raise ValueError(f"{dataset_name}: missing column 'dataset_source'")

    speakers_df = clean_col(speakers_df, "dataset_source", "unknown")
    speakers_df = clean_col(speakers_df, "gender", "unknown")
    speakers_df = clean_col(speakers_df, "source_age", "unknown")

    if dataset_name in ["adult_real_candidates", "adult_spoof_targets", "adult_backup"]:
        if "age_bin" not in speakers_df.columns:
            raise ValueError(f"{dataset_name}: missing column 'age_bin'")
        return (
            speakers_df["dataset_source"] + "|" +
            speakers_df["gender"] + "|" +
            speakers_df["age_bin"]
        )

    elif dataset_name in ["minor_real_candidates", "minor_spoof_targets", "minor_backup"]:
        return (
            speakers_df["dataset_source"] + "|" +
            speakers_df["gender"]
        )

    else:
        raise ValueError(f"Unknown dataset name: {dataset_name}")


# ─── Split Size Assignment ───────────────────────────────
def assign_counts(
    n: int,
    train_ratio: float = 0.70,
    val_ratio: float = 0.15,
    rng: np.random.Generator | None = None
) -> tuple[int, int, int]:
    """
    Determine the number of samples assigned to each split
    within a single stratum.

    - Uses probabilistic assignment for small groups.
    - Uses proportional allocation with rounding correction otherwise.
    """
    if n <= 0:
        return 0, 0, 0

    if n <= 5:
        probs = [train_ratio, val_ratio, 1 - train_ratio - val_ratio]
        choices = rng.choice(["train", "val", "test"], size=n, p=probs)
        return (
            (choices == "train").sum(),
            (choices == "val").sum(),
            (choices == "test").sum()
        )

    target_train = n * train_ratio
    target_val   = n * val_ratio
    target_test  = n - target_train - target_val

    n_train = int(np.floor(target_train))
    n_val   = int(np.floor(target_val))
    n_test  = int(np.floor(target_test))

    remaining = n - (n_train + n_val + n_test)

    remainders = [
        ("train", target_train - n_train),
        ("val",   target_val - n_val),
        ("test",  target_test - n_test),
    ]

    rng.shuffle(remainders)
    remainders.sort(key=lambda x: x[1], reverse=True)

    for i in range(remaining):
        if remainders[i][0] == "train":
            n_train += 1
        elif remainders[i][0] == "val":
            n_val += 1
        else:
            n_test += 1

    return n_train, n_val, n_test


# ─── Speaker-Disjoint Split ──────────────────────────────
def speaker_disjoint_stratified_split(
    df: pd.DataFrame,
    dataset_name: str,
    train_ratio: float = 0.70,
    val_ratio: float = 0.15,
    seed: int = 42
) -> pd.DataFrame:
    """
    Perform a stratified split while ensuring:
    - No speaker appears in more than one split.
    - Data distribution is preserved across splits.
    """
    rng = np.random.default_rng(seed)
    df = clean_dataframe(df)

    speakers = df.copy()
    speakers["strat_key"] = build_strat_key(speakers, dataset_name)
    speakers["split"] = ""

    for _, grp in speakers.groupby("strat_key", sort=False):
        speaker_ids = grp["speaker_id"].tolist()
        rng.shuffle(speaker_ids)

        n_train, n_val, n_test = assign_counts(
            n=len(speaker_ids),
            train_ratio=train_ratio,
            val_ratio=val_ratio,
            rng=rng
        )

        speakers.loc[speakers["speaker_id"].isin(speaker_ids[:n_train]), "split"] = "train"
        speakers.loc[speakers["speaker_id"].isin(speaker_ids[n_train:n_train+n_val]), "split"] = "val"
        speakers.loc[speakers["speaker_id"].isin(speaker_ids[n_train+n_val:]), "split"] = "test"

    if (speakers["split"] == "").any():
        raise ValueError(f"{dataset_name}: unassigned speakers detected")

    return speakers


# ─── Split Validation ────────────────────────────────────
def report_split_stats(split_df: pd.DataFrame, dataset_name: str) -> None:
    """
    Verify split integrity and report distribution.

    Checks:
    - No overlap between splits (no speaker leakage).
    - Displays counts and percentages.
    """
    sp = split_df.copy()

    train_speakers = set(sp.loc[sp["split"] == "train", "speaker_id"])
    val_speakers   = set(sp.loc[sp["split"] == "val", "speaker_id"])
    test_speakers  = set(sp.loc[sp["split"] == "test", "speaker_id"])

    if (train_speakers & val_speakers) or \
       (train_speakers & test_speakers) or \
       (val_speakers & test_speakers):
        raise ValueError(f"{dataset_name}: speaker leakage detected")

    total = len(sp)
    print("\nSpeaker counts:")
    for s in ["train", "val", "test"]:
        n = (sp["split"] == s).sum()
        print(f"{s}: {n} ({(n/total)*100:.2f}%)")


# ─── Save Results ────────────────────────────────────────
def save_inventory_with_split(split_df, dataset_name, out_dir):
    """
    Save the processed dataset with assigned splits.
    """
    os.makedirs(out_dir, exist_ok=True)

    cols_to_drop = ["strat_key", "age_bin"]
    split_df = split_df.drop(columns=[c for c in cols_to_drop if c in split_df.columns])

    path = os.path.join(out_dir, f"{dataset_name}_split.csv")
    split_df.to_csv(path, index=False)
    return path


# ─── Full Pipeline ───────────────────────────────────────
def run_speaker_split_pipeline(input_files, output_dir, train_ratio=0.70, val_ratio=0.15, seed=42):
    """
    Execute the complete pipeline:
    - Load dataset
    - Perform speaker-disjoint stratified split
    - Validate results
    - Save outputs
    """
    os.makedirs(output_dir, exist_ok=True)

    for dataset_name, file_path in input_files.items():
        print("\n" + "=" * 60)
        print(f"Processing: {dataset_name}")
        print("=" * 60)

        df = pd.read_csv(file_path, dtype={"speaker_id": str})
        df["speaker_id"] = df["speaker_id"].astype(str).str.strip()

        if "dataset_source" in df.columns:
            myst_mask = df["dataset_source"] == "myst"
            df.loc[myst_mask, "speaker_id"] = df.loc[myst_mask, "speaker_id"].str.zfill(6)

        split_df = speaker_disjoint_stratified_split(df, dataset_name, train_ratio, val_ratio, seed)

        report_split_stats(split_df, dataset_name)
        save_inventory_with_split(split_df, dataset_name, output_dir)

"""
Split Half - Speaker-Level Balanced Splitting
----------------------------------------------

Objective
---------
Partition a dataframe into two equal halves (A and B) while:
- Ensuring no speaker appears in both halves (no data leakage).
- Balancing segment counts between the two halves.
- Preserving age class distribution across both halves.

Methodology
-----------
1. Compute per-speaker segment count and age class.
2. Try n_splits random 50/50 speaker partitions.
3. Score each partition based on:
   - Segment count difference between A and B.
   - Age class distribution difference between A and B.
4. Select the partition with the lowest combined score.
5. Save both halves as CSV files.

Parameters
----------
df          : pd.DataFrame  Input metadata dataframe.
split_name  : str           Prefix for output files (e.g., 'train').
output_dir  : str           Directory to save output CSV files.
n_splits    : int           Number of random splits to try (default: 200).
seed        : int           Random seed for reproducibility (default: 42).

Output
------
- {output_dir}/{split_name}_real_A.csv
- {output_dir}/{split_name}_real_B.csv

Returns
-------
a_df, b_df : pd.DataFrame, pd.DataFrame
"""

def split_half(df, split_name, output_dir, n_splits=200, seed=42):
    # Build speaker-level summary with segment count and age class
    speaker_info = (
        df.groupby("speaker_id")
        .agg(
            n_segments=("speaker_id", "size"),
            mapped_age_class=("mapped_age_class", "first")
        )
        .reset_index()
    )

    # Try n_splits random 50/50 speaker splits and pick the best one
    gss = GroupShuffleSplit(n_splits=n_splits, test_size=0.5, random_state=seed)
    best_score = float("inf")
    best_a_speakers = None
    best_b_speakers = None

    for a_idx, b_idx in gss.split(speaker_info, groups=speaker_info["speaker_id"]):
        a = speaker_info.iloc[a_idx]
        b = speaker_info.iloc[b_idx]

        # Criterion 1: segment count balance
        seg_diff = abs(a["n_segments"].sum() - b["n_segments"].sum())

        # Criterion 2: age class distribution balance
        a_age = a["mapped_age_class"].value_counts(normalize=True)
        b_age = b["mapped_age_class"].value_counts(normalize=True)
        all_ages = set(a_age.index) | set(b_age.index)
        age_diff = sum(abs(a_age.get(v, 0) - b_age.get(v, 0)) for v in all_ages)

        # Combined score (age balance weighted higher)
        score = seg_diff + (age_diff * 100)

        if score < best_score:
            best_score = score
            best_a_speakers = a["speaker_id"]
            best_b_speakers = b["speaker_id"]

    # Filter original df by best speakers (no speaker overlap guaranteed)
    a_df = df[df["speaker_id"].isin(best_a_speakers)].reset_index(drop=True)
    b_df = df[df["speaker_id"].isin(best_b_speakers)].reset_index(drop=True)

    # Save metadata CSVs
    os.makedirs(output_dir, exist_ok=True)
    a_df.to_csv(f"{output_dir}/{split_name}_real_A.csv", index=False)
    b_df.to_csv(f"{output_dir}/{split_name}_real_B.csv", index=False)

    # Verify
    overlap = set(a_df["speaker_id"]) & set(b_df["speaker_id"])
    print(f"{split_name}: A={len(a_df)}, B={len(b_df)}, diff={abs(len(a_df)-len(b_df))}, overlap={len(overlap)}")

    return a_df, b_df
